class Pyramid5

{
	
public static void main(String args[])
{	
	for(int i=1;i<=9;i++)
	{  
int a =8;
int b =(10-i);
		for(int j=8;j>=i;j--)
		{
			
			
			System.out.print(" ");
			
		}
		for(int k=1;k<=i;k++)
		{
			
			
			System.out.print(b);
			b++;
		}
		
		for(int l=1;l<=i-1;l++)
			
		{
			
			
			System.out.print(a);
           
			a--;
		}
		
		
	
		{
			
		System.out.println(" ");
	
		}
		
			
	}
	
		
	}

}